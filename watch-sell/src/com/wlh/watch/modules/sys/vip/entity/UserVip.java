package com.wlh.watch.modules.sys.vip.entity;

public class UserVip {
	private String id;
	private String vipLevel;
	private int vipCredits;
	private String vipDiscount;
	private String vipName;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getVipLevel() {
		return vipLevel;
	}
	public void setVipLevel(String vipLevel) {
		this.vipLevel = vipLevel;
	}
	public int getVipCredits() {
		return vipCredits;
	}
	public void setVipCredits(int vipCredits) {
		this.vipCredits = vipCredits;
	}
	public String getVipDiscount() {
		return vipDiscount;
	}
	public void setVipDiscount(String vipDiscount) {
		this.vipDiscount = vipDiscount;
	}
	public String getVipName() {
		return vipName;
	}
	public void setVipName(String vipName) {
		this.vipName = vipName;
	}
	
}
